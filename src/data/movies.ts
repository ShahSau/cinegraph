export const getTrendingMovies = (): Movie[] => movies.filter(m => m.popularity > 90);
export const getPopularMovies = (): Movie[] => movies.filter(m => m.popularity > 85);
export const getTopRatedMovies = (): Movie[] => movies.filter(m => m.rating.imdb >= 8.0);
export const getUpcomingMovies = (): Movie[] => movies.filter(m => m.status === 'Upcoming' || m.year >= 2024);
export const getMovieById = (id: number): Movie | undefined => movies.find(m => m.id === id);
export const getSimilarMovies = (movie: Movie): Movie[] => {
  return movies.filter(m => 
    m.id !== movie.id && 
    m.genres.some(g => movie.genres.includes(g))
  ).slice(0, 6);
};
export const getCastByMovieId = (id: number): CastMember[] => castData[id] || castData[1];
export const searchMovies = (query: string): Movie[] => {
  const lowerQuery = query.toLowerCase();
  return movies.filter(m => 
    m.title.toLowerCase().includes(lowerQuery) ||
    m.genres.some(g => g.toLowerCase().includes(lowerQuery)) ||
    m.director?.toLowerCase().includes(lowerQuery)
  );
};