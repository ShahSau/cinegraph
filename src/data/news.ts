export const getNewsById = (id: string): NewsArticle | undefined => {
  return newsArticles.find(article => article.id === id);
};

export const getNewsByCategory = (category: NewsCategory): NewsArticle[] => {
  return newsArticles.filter(article => article.category === category);
};

export const getFeaturedNews = (): NewsArticle | undefined => {
  return newsArticles.find(article => article.featured);
};

export const getTrendingNews = (): NewsArticle[] => {
  return newsArticles.slice(0, 5);
};

export const getLatestNews = (): NewsArticle[] => {
  return [...newsArticles].sort(
    (a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  ).slice(0, 6);
};

export const getRelatedNews = (currentId: string, category: NewsCategory): NewsArticle[] => {
  return newsArticles
    .filter(article => article.id !== currentId && article.category === category)
    .slice(0, 4);
};